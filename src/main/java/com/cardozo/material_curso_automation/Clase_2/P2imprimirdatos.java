package com.cardozo.material_curso_automation.Clase_2;

public class P2imprimirdatos {

    public static void main(String[] args) {

        System.out.println(10); // esto es un entero (int)
        System.out.println(165.20); // esto es un flotante (float)
        System.out.println('p'); // esto es un character (char)
        System.out.println('8'); // esto es un char
        System.out.println(8); // esto es un entero
        System.out.println("Hello and welcome!"); // esto es una cadena (string)
        System.out.println(true); // esto es un buleano
        System.out.println(false); // esto es un buleano
    }
}
